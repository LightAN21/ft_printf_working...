#include "ft_printf.h"

void	save_str(t_var *v, char *s, t_max len)
{
	t_max	i;

	i = -1;
	while (++i < len)
	{
		v->p[v->k++] = s[i];
		if (v->k >= 10000)
		{
			write(1, v->p, v->k);
			v->return_value += v->k;
			v->k = 0;
		}
	}
}
